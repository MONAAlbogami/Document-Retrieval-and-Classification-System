def assess_severity(symptoms_count, duration_str):
    """
    تقييم خطورة الحالة بناءً على عدد الأعراض ومدة المرض
    """
    # تحويل المدة لرقم (أيام)
    duration_days = 0
    if duration_str:
        d = duration_str.lower()
        if "less than 1" in d:
            duration_days = 0.5
        elif "1–3" in d or "1-3" in d:
            duration_days = 2
        elif "4–7" in d or "4-7" in d:
            duration_days = 5
        elif "1–2 weeks" in d or "1-2 weeks" in d:
            duration_days = 10
        elif "more than 2" in d:
            duration_days = 20

    # منطق التقييم
    if symptoms_count <= 1 and duration_days <= 2:
        level = "low"
        label = "حالة طبيعية"
        label_en = "Normal"
        desc = "الأعراض خفيفة ولا تستدعي القلق. راقبي الوضع."
        color = "#5BB8A6"
        icon = "🟢"

    elif symptoms_count == 2 or (symptoms_count <= 3 and duration_days <= 5):
        level = "moderate"
        label = "خطورة متوسطة"
        label_en = "Moderate Risk"
        desc = "الأعراض تستدعي الانتباه. يُنصح بالراحة ومتابعة الحالة."
        color = "#D4A840"
        icon = "🟡"

    elif symptoms_count >= 3 and duration_days >= 5:
        level = "critical"
        label = "حالة حرجة"
        label_en = "Critical"
        desc = "الأعراض متعددة وممتدة. يجب مراجعة الطبيب فوراً."
        color = "#D45878"
        icon = "🔴"

    else:
        level = "warning"
        label = "تحتاج متاب<seg_125>لعة"
        label_en = "Needs Attention"
        desc = "راجعي الطبيب إذا استمرت الأعراض أكثر من يومين."
        color = "#E07850"
        icon = "🟠"

    return {
        "level": level,
        "label": label,
        "label_en": label_en,
        "desc": desc,
        "color": color,
        "icon": icon,
        "symptoms_count": symptoms_count,
        "duration_days": duration_days
    }

