def tokenize_and_remove_stopwords(text):
    # lowercase
    text = text.lower()
    # إزالة علامات الترقيم
    text = text.translate(str.maketrans('', '', string.punctuation))
    # تقسيم إلى كلمات (Tokenization)
    tokens = text.split()
    # حذف الـ stopwords والأرقام
    tokens = [w for w in tokens if w not in stop_words and not w.isdigit()]
    return tokens

