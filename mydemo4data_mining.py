import re

def assign_label(filename):
    # استخراج الرقم بأمان بغض النظر عن - أو _
    match = re.search(r'doc(\d+)', filename)
    if match:
        num = int(match.group(1))
        return "Medical" if num <= 15 else "Non-Medical"
    return "Unknown"

